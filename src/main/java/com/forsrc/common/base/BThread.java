package com.forsrc.common.base;

public abstract class BThread implements Runnable {

  private static final int threadInterval_default = 1000;  //

  private int threadInterval;  //

  private boolean stop = false;

  public BThread() {
    threadInterval = threadInterval_default;
  }

  //<<---------------------------------------- initialize ----------------------------------------

  //>>---------------------------------------- initialize ----------------------------------------

  //<<---------------------------------------- public ----------------------------------------
  public void stop() {

  }

  public synchronized void setStop(boolean stop) {
    this.stop = stop;
  }

  public synchronized boolean isStop() {
    return this.stop;
  }

  //>>---------------------------------------- public ----------------------------------------

  //<<---------------------------------------- protected ----------------------------------------

  protected void setThreadInterval(int threadInterval) {
    this.threadInterval = threadInterval;
    if (this.threadInterval <= 0) {
      this.threadInterval = threadInterval_default;
    }
  }

  protected void sleep(int threadInterval) {
    try {
      Thread.sleep(threadInterval);
    } catch (Exception e) {
    }
  }

  protected void sleep() {
    sleep(threadInterval);
  }

  //>>---------------------------------------- protected ----------------------------------------

  //<<---------------------------------------- private ----------------------------------------

  //<<<---------------------------------------- inner ----------------------------------------

  //>>>---------------------------------------- inner ----------------------------------------

  //<<<---------------------------------------- tool ----------------------------------------

  //>>>---------------------------------------- tool ----------------------------------------

  //>>---------------------------------------- private ----------------------------------------

  //<<---------------------------------------- get ----------------------------------------

  //>>---------------------------------------- get ----------------------------------------

  //<<---------------------------------------- set ----------------------------------------

  //>>---------------------------------------- set ----------------------------------------

  //<<---------------------------------------- get set ----------------------------------------

  //>>---------------------------------------- get set ----------------------------------------
}